import Fs from 'node:fs';
import Path from 'node:path';
import Sharp from 'sharp';
import { singleton } from 'tsyringe';

type ReadImage = {
  sharpInstance: Sharp.Sharp;
  stats: Sharp.Stats;
}

@singleton()
export default class BestVideoThumbnailFrameSelector {
  async determineBestFrame(directoryPath: string): Promise<Sharp.Sharp> {
    const frameImages = await this.findFrameImages(directoryPath);
    if (frameImages.length === 0) {
      throw new Error(`No frame images found in directory: ${directoryPath}`);
    }

    let bestFrame: Sharp.Sharp;
    let bestScore = -1;
    for (const frameImagePromise of frameImages) {
      const frameImage = await frameImagePromise;
      const score = this.calculateThumbnailScore(frameImage.stats);

      if (score > bestScore) {
        bestFrame = frameImage.sharpInstance;
        bestScore = score;
      }
    }

    return bestFrame!;
  }

  private async findFrameImages(directoryPath: string): Promise<Promise<ReadImage>[]> {
    const results: Promise<ReadImage>[] = [];

    const dirHandle = await Fs.promises.opendir(directoryPath);
    for await (const dirent of dirHandle) {
      if (!dirent.name.endsWith('.png')) {
        continue;
      }

      results.push(this.readImage(Path.join(dirent.parentPath, dirent.name)));
    }

    return results;
  }

  private async readImage(imagePath: string): Promise<ReadImage> {
    const sharp = Sharp(await Fs.promises.readFile(imagePath));
    return {
      sharpInstance: sharp,
      stats: await sharp.stats(),
    };
  }

  /**
   * @author Generated by a large language model. Keeping its comments for future reference.
   */
  private calculateThumbnailScore(stats: Sharp.Stats): number {
    // Sharpness:
    // Sharpness is often the most critical factor for thumbnails.
    // Sharpness values can vary wildly, but generally >10 is okay, >20 is good.
    // We cap it at 30 to prevent over-sharpened noisy images from winning.
    const normalizedSharpness = Math.min(stats.sharpness, 30) / 30;

    // Entropy:
    // Entropy usually falls between 0 (flat color) and 8 (random noise).
    // A good image usually has entropy between 6 and 7.5.
    const normalizedEntropy = Math.min(stats.entropy, 8) / 8;

    // Contrast:
    // Sharp stats doesn't give "contrast" directly, but standard deviation (stdev)
    // of the channels is a standard mathematical proxy for RMS Contrast.
    // Average the stdev across RGB channels (ignoring Alpha if present).
    const avgStdev = stats.channels
      .slice(0, 3)
      .reduce((sum, c) => sum + c.stdev, 0) / 3;

    // A standard deviation of ~50-60 is usually good contrast. <30 is flat.
    const normalizedContrast = Math.min(avgStdev, 70) / 70;

    // Brightness / Luminance:
    // We want to avoid images that are too dark or blown out.
    // Ideal luminance is often around mid-high (approx 100-150 out of 255).
    const avgMean = stats.channels
      .slice(0, 3)
      .reduce((sum, c) => sum + c.mean, 0) / 3;

    // Calculate distance from ideal brightness (e.g., 128).
    // 1 - (distance / max_distance) gives us a score where 1 is ideal.
    const idealBrightness = 128;
    const brightnessScore = 1 - (Math.abs(avgMean - idealBrightness) / 128);

    // Saturation / Colorfulness:
    // If R, G, and B means are identical, the image is grayscale.
    // If they diverge, the image has color.
    const means = stats.channels.slice(0, 3).map(c => c.mean);
    const maxMean = Math.max(...means);
    const minMean = Math.min(...means);

    // A simple proxy for saturation is the difference between max and min channel averages.
    // Range is 0 to ~255, but practically >50 is quite colorful.
    const saturationProxy = maxMean - minMean;
    const normalizedSaturation = Math.min(saturationProxy, 60) / 60;

    // You can adjust these weights based on your preferences.
    return (normalizedSharpness * 0.40) +
      (normalizedEntropy * 0.20) +
      (normalizedContrast * 0.20) +
      (brightnessScore * 0.10) +
      (normalizedSaturation * 0.10);
  }
}
